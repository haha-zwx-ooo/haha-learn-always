package com.haha.day4;

import lombok.extern.slf4j.Slf4j;

/**
 * @author zwx18
 * @version 1.0
 **/
@Slf4j
public class Operation3 {
    public static void main(String[] args) {
        //==
        System.out.println(1 == 1);
        //!=
        System.out.println(1 != 2);
        //>
        System.out.println(1 > 2);
        //<
        System.out.println(1 < 2);
        //<=
        System.out.println(1 <= 2);
        //>=
        System.out.println(1 >= 2);
    }
}
